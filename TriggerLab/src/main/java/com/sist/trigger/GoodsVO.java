package com.sist.trigger;

public class GoodsVO {
	private String 상품코드;
	private String 상품명;
	private String 제조사;
	private String 소비자가격;
	private String 재고수량;
	
	public String get상품코드() {
		return 상품코드;
	}
	public void set상품코드(String 상품코드) {
		this.상품코드 = 상품코드;
	}
	public String get상품명() {
		return 상품명;
	}
	public void set상품명(String 상품명) {
		this.상품명 = 상품명;
	}
	public String get제조사() {
		return 제조사;
	}
	public void set제조사(String 제조사) {
		this.제조사 = 제조사;
	}
	public String get소비자가격() {
		return 소비자가격;
	}
	public void set소비자가격(String 소비자가격) {
		this.소비자가격 = 소비자가격;
	}
	public String get재고수량() {
		return 재고수량;
	}
	public void set재고수량(String 재고수량) {
		this.재고수량 = 재고수량;
	}	
}
